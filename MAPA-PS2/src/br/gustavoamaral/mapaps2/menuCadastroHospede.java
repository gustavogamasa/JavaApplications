    public void confirmarHopedagemValida(){
    
    Double valorTotal;
    Dados d = new Dados();
    valorTotal = d.retornarValorDiaria(Integer.valueOf(comboBoxQuarto.getSelectedItem().toString()));
    
    valorTotal = valorTotal * Integer.valueOf(txtDiarias.getText());
    
    if (Integer.valueOf(txtDiarias.getText())>7) valorTotal = valorTotal - ((valorTotal/100)*10);
    
    textoErroQuantidade.setVisible(false);
    
    JOptionPane.showMessageDialog(null, "Hospedagem cadastrada com sucesso!\nQuarto número: "+comboBoxQuarto.getSelectedItem().toString()+
            "\nHóspede: "+txtNome.getText()+
            "\nDiárias: "+txtDiarias.getText()+
            "\nQuantidade de hóspedes: "+ (Integer.valueOf(txtQuantidadeTOTALHospedes1.getText()) - (Integer.valueOf(txtBebes2anos.getText())))+        
            "\nValor final: R$"+valorTotal);
        
        this.dispose();
        
        
    }
    
    public void cadastrarHospedagem(){
    
    Integer numeroQuartoEscolhido = Integer.valueOf(comboBoxQuarto.getSelectedItem().toString());
   
        switch (numeroQuartoEscolhido) {
            case 1:
                {
                    int numeroHospedes = (Integer.valueOf(txtQuantidadeTOTALHospedes1.getText()) - (Integer.valueOf(txtBebes2anos.getText())));
                    if (numeroHospedes > 1) {textoErroQuantidade.setVisible(true);}
                    else //qnt hospedes ok
                    {
                        textoErroQuantidade.setVisible(false);
                        
                        Dados d = new Dados();
                        Hospedes h = new Hospedes(txtNome.getText(), txtIdade.getText(), txtEndereco.getText());
                        d.cadastrarHospede(h);
                        d.atualizarQuarto(numeroQuartoEscolhido);
                        confirmarHopedagemValida();
                        
                    }//
                    preencherTabela();
                  
                    break;
                } // quarto 1
            case 2:
                {
                    int numeroHospedes = (Integer.valueOf(txtQuantidadeTOTALHospedes1.getText()) - (Integer.valueOf(txtBebes2anos.getText())));
                    if (numeroHospedes > 2) {textoErroQuantidade.setVisible(true);}
                    else //qnt hospedes ok
                    {
                        textoErroQuantidade.setVisible(false);
                        
                        Dados d = new Dados();
                        Hospedes h = new Hospedes(txtNome.getText(), txtIdade.getText(), txtEndereco.getText());
                        d.cadastrarHospede(h);
                        d.atualizarQuarto(numeroQuartoEscolhido);
                        confirmarHopedagemValida();
                        
                    }//
                    preencherTabela();
                    
                    break;
                }
            case 3:
                {
                    int numeroHospedes = (Integer.valueOf(txtQuantidadeTOTALHospedes1.getText()) - (Integer.valueOf(txtBebes2anos.getText())));
                    if (numeroHospedes > 3) {textoErroQuantidade.setVisible(true);}
                    else //qnt hospedes ok
                    {
                        textoErroQuantidade.setVisible(false);
                        
                        Dados d = new Dados();
                        Hospedes h = new Hospedes(txtNome.getText(), txtIdade.getText(), txtEndereco.getText());
                        d.cadastrarHospede(h);
                        d.atualizarQuarto(numeroQuartoEscolhido);
                        confirmarHopedagemValida();
                        
                    }//
                    preencherTabela();
                    
                    break;
                }
            case 4:
                {
                    int numeroHospedes = (Integer.valueOf(txtQuantidadeTOTALHospedes1.getText()) - (Integer.valueOf(txtBebes2anos.getText())));
                    if (numeroHospedes > 2) {textoErroQuantidade.setVisible(true);}
                    else //qnt hospedes ok
                    {
                        textoErroQuantidade.setVisible(false);
                        
                        Dados d = new Dados();
                        Hospedes h = new Hospedes(txtNome.getText(), txtIdade.getText(), txtEndereco.getText());
                        d.cadastrarHospede(h);
                        d.atualizarQuarto(numeroQuartoEscolhido);
                        confirmarHopedagemValida();
                        
                    }//
                    preencherTabela();
                  
                    break;
                }
            default:
                break;
        }
    
      preencherTabela();
    
    }//cadastro
    
    

