        private void preencherTabela(){
        
        JTable tabela = new JTable();
        ArrayList<Quartos> quarto = new ArrayList<>();
        Dados d = new Dados();
        quarto = d.retornarQuartos();
        DefaultTableModel modelo = (DefaultTableModel) tableQuartosCadastro.getModel();

        modelo.setNumRows(0);
              
        for (int i = 0; i < d.contarQuartos(); i++) {
    
         
        modelo.addRow(new Object[]{
        quarto.get(i).getQuartoNumero(),
        quarto.get(i).getQuartoTipo(),
        quarto.get(i).getQuartoCapacidade().toString(),
        quarto.get(i).getQuartoValorDiaria().toString(),
        quarto.get(i).getQuartoDisponivel()
               
        });  

        }
        tableQuartosCadastro.setModel(modelo);
        tableQuartosCadastro.repaint();
        
       
    
    }
    
    
    
